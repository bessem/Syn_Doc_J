    private void create_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_BtnActionPerformed
        Patient p = new Patient();
        
        try {
        p.setId(Long.parseLong(idTextField.getText()));
        p.setFname(fnameTextField.getText());
        p.setLname(lnameTextField.getText());
        p.setTel(telTextField.getText());
        p.setBday(parseDate(dateTextField.getText()));
        p.setGender(getGendre());
        manager.getTransaction().begin();
        manager.persist(p);
        manager.getTransaction().commit();
        resetFields();
        } catch (Exception e) {
            manager.getTransaction().rollback();
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());         
        }
    }//GEN-LAST:event_create_BtnActionPerformed
    private void resetFields(){
        idTextField.setText("");
        fnameTextField.setText("");
        lnameTextField.setText("");
        telTextField.setText("");
        dateTextField.setText("");
    }
    private void search_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_BtnActionPerformed
        if(id_Field.getText().length()>0){
            Patient patient = new Patient();
            patient = manager.find(Patient.class,Long.parseLong(id_Field.getText()));
            try{
                    System.out.println("Found"+patient.toString());
                    resultArea.setText(patient.toString());
                    consul_Btn.setVisible(true);
                    id = Long.parseLong(id_Field.getText());
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());   
            }
        }else{
             JOptionPane.showMessageDialog(this, "No ID Criteria to perform search !");   
        }
    }//GEN-LAST:event_search_BtnActionPerformed

