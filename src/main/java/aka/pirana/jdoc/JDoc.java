    private String getGendre(){
      if(maleCheckBox.isEnabled()){
          return "Male";
      }else{
          return "Female";
      }
        
    }
    
    private Date parseDate(String dateStr){
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
           Date date= new Date();
           try {
               date = dateFormat.parse(dateStr);
           } catch (ParseException e) {
               e.printStackTrace();
           }
           return date;
    }
    
